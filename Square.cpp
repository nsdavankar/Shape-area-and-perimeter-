/**************************************************************************
* Square::Square()
* -------------------------------------------------------------------------
* The constructor for Square class
* -------------------------------------------------------------------------
* Input: 
*		double doubleSquare
**************************************************************************/
Square::Square(double doubleSquare)
{
	squareSide = doubleSquare;
}

/**************************************************************************
* Square::~Square()
* -------------------------------------------------------------------------
* The destructor for Square class
* -------------------------------------------------------------------------
**************************************************************************/
Square::~Square()
{
}

/**************************************************************************
* void Square:: calcPerimeter()
* -------------------------------------------------------------------------
* This function calculates the perimeter for the Square class
* -------------------------------------------------------------------------
**************************************************************************/
void Square:: calcPerimeter()const
{
	double perimeterSquare = 0;

	perimeterSquare = 4* squareSide;

	cout << "Square Perimeter: " << perimeterSquare << endl;
}

/**************************************************************************
* void Square:: calcArea()
* -------------------------------------------------------------------------
* This function calculates the area for the Square class
* -------------------------------------------------------------------------
**************************************************************************/
void Square::calcArea()const
{
	double areaSquare = 0;

	areaSquare = pow(squareSide, 2);

	cout << "Square Area: " << areaSquare << endl;
}
